<script>
export default {
  data() {
    return {
      informationObj: {
        full_name: "",
        phone_number: "",
      },
      makeButtonLoading: false,
    };
  },
  methods: {
    async checkAndRegisterInNewsletter(e) {
      e.preventDefault();
      if (this.informationObj.full_name.trim() === "")
        return this.$notify({
          title: "خطا!",
          text: "لطفا نام و نام خانوادگی خود را وارد کنید.",
          type: "error",
        });
      if (this.informationObj.phone_number.trim() === "")
        return this.$notify({
          title: "خطا!",
          text: "لطفا شماره تلفن همراه خود را وارد کنید.",
          type: "error",
        });
      if (this.informationObj.phone_number.length !== 11)
        return this.$notify({
          title: "خطا!",
          text: "لطفا شماره تلفن همراه خود رابه درستی وارد کنید.",
          type: "error",
        });
      this.makeButtonLoading = true;
      document.getElementById("news_letter_btn").style.opacity = 0;
      await this.$axios
        .post("/add_new_receiver", this.informationObj)
        .then((response) => {
          this.makeButtonLoading = false;
          return this.$notify({
            title: "سپاس از مشارکت شما!",
            text: response.data.msg,
            type: "success",
          });
        })
        .catch(() => {
          this.makeButtonLoading = false;
          document.getElementById("news_letter_btn").style.opacity = 1;
          return this.$notify({
            title: "عملیات ناموفق!",
            text: "خطایی در ثبت اطلاعات رخ داد.",
            type: "error",
          });
        });
    },
  },
};
